var input = 1;
var digit;

switch(input) {
    case 0:
        digit = "Zero";
        break;
    case 1:
        digit = "One";
        break;
    case 2:
        digit = "Two";
        break;
    case 3:
        digit = "Three";
        break;
    case 4:
        digit = "Four";
        break;
    case 5:
        digit = "Five";
        break;           
    case 6:
        digit = "Six";
        break;         
    case 7:
        digit = "Seven";
        break;
    case 8:
        digit = "Eight";
        break;
    case 9:
        digit = "Nine";
        break;
    default:
        digit = "You have entered a wrong input";                    
}
console.log(digit);